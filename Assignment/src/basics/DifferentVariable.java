package basics;

public class DifferentVariable {

	public static void main(String[] args) {
		int a=10;
		boolean b=true;
		char c='A';
		double d=1.25;
		float f=20;
		
		System.out.println("a: "+a);
		System.out.println("b: "+b);
		System.out.println("c: "+c);
		System.out.println("d: "+d);
		System.out.println("f: "+f);
	}

}
